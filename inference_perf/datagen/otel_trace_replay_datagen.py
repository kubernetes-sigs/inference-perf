# ---------------------------------------------------------------------------
# WorkerSessionTracker — tracks session state within a worker process
# ---------------------------------------------------------------------------


class WorkerSessionTracker:
    """Tracks session state within a single worker process.

    Since all nodes of a session run on the same worker (session-to-worker affinity),
    each worker can independently track:
    - Which nodes have completed
    - Whether the session has failed
    - Node completion times

    Session completion is communicated to main process via mp.Queue.
    """

    def __init__(self):
        """Initialize empty tracking state."""
        # Nested map: session_id -> {node_id -> completion_time}
        self._node_completions: Dict[str, Dict[str, float]] = {}

        # Set of session_ids that have failed
        self._failed_sessions: Set[str] = set()

    def record_node_completed(self, session_id: str, node_id: str, completion_time: float) -> None:
        """Record that a node has completed.

        Args:
            session_id: The session ID
            node_id: The node ID (unqualified, e.g., "node_0")
            completion_time: Wall-clock time when node completed
        """
        if session_id not in self._node_completions:
            self._node_completions[session_id] = {}
        self._node_completions[session_id][node_id] = completion_time

    def is_node_completed(self, session_id: str, node_id: str) -> bool:
        """Check if a node has completed.

        Args:
            session_id: The session ID
            node_id: The node ID (unqualified, e.g., "node_0")

        Returns:
            True if the node has completed
        """
        return session_id in self._node_completions and node_id in self._node_completions[session_id]

    def get_node_completion_time(self, session_id: str, node_id: str) -> Optional[float]:
        """Get the completion time for a node.

        Args:
            session_id: The session ID
            node_id: The node ID (unqualified, e.g., "node_0")

        Returns:
            Completion time, or None if not completed
        """
        return self._node_completions.get(session_id, {}).get(node_id)

    def mark_session_failed(self, session_id: str) -> None:
        """Mark a session as failed.

        Args:
            session_id: The session ID
        """
        self._failed_sessions.add(session_id)

    def is_session_failed(self, session_id: str) -> bool:
        """Check if a session has failed.

        Args:
            session_id: The session ID

        Returns:
            True if the session has failed
        """
        return session_id in self._failed_sessions

    def get_session_node_count(self, session_id: str) -> int:
        """Get the number of completed nodes for a session.

        Args:
            session_id: The session ID

        Returns:
            Number of completed nodes
        """
        return len(self._node_completions.get(session_id, {}))

    def get_session_completion_times(self, session_id: str) -> Dict[str, float]:
        """Get all node completion times for a session.

        Args:
            session_id: The session ID

        Returns:
            Dict mapping node_id to completion_time
        """
        return self._node_completions.get(session_id, {}).copy()


# ---------------------------------------------------------------------------
# NodeOutputRegistry — intra-worker output and message store
# ---------------------------------------------------------------------------


class NodeOutputRegistry:
    """Per-worker registry mapping node_id → actual output text and input messages.

    All data is stored in plain in-process dicts. Session-to-worker affinity
    guarantees that the node writing an output and the nodes reading it always
    run on the same worker process, so no IPC is needed here.

    Async waiting between coroutines on the same event loop is handled via
    asyncio.Event — one per node, allocated on first demand.

    This class has no Manager dependency. Cross-process communication for session
    completion is handled via mp.Queue, not shared state.

    Usage:
        registry = NodeOutputRegistry()
        # workers call registry.record(node_id, text, messages) after each LLM call
        # dependent nodes call await registry.require_async(node_id) before dispatching
    """

    def __init__(self) -> None:
        # Output text and input messages: plain dicts, same-worker only.
        self._node_output_text: Dict[str, str] = {}
        self._node_input_messages: Dict[str, Any] = {}

        # One asyncio.Event per node. Only accessed from coroutines on this
        # worker's single event loop; no lock needed.
        self._node_events: Dict[str, asyncio.Event] = {}

    def record(self, node_id: str, output_text: str, messages: List[Any]) -> None:
        """Register output for a completed node and wake any async waiters."""
        self._node_output_text[node_id] = output_text
        if messages:
            self._node_input_messages[node_id] = list(messages)

        # Signal any coroutine waiting on this node (same worker only).
        if node_id in self._node_events:
            self._node_events[node_id].set()
            logger.debug(f"Set asyncio.Event for node {node_id}")

    def get(self, node_id: str) -> Optional[str]:
        """Return the output text for node_id, or None if not yet registered."""
        return self._node_output_text.get(node_id)

    def get_output_by_node_id(self, node_id: str) -> Optional[str]:
        """Return the output text for a given node_id."""
        return self._node_output_text.get(node_id)

    def get_messages_by_node_id(self, node_id: str) -> Optional[List[Any]]:
        """Return the input messages for a given node_id.

        Args:
            node_id: The node ID to look up

        Returns:
            List of input messages, or None if not found
        """
        return self._node_input_messages.get(node_id)

    def get_node_ids(self) -> List[str]:
        """Return all registered node IDs.

        Returns:
            List of all node IDs that have registered outputs
        """
        return list(self._node_output_text.keys())

    async def require_async(self, node_id: str, timeout_sec: float = 3600.0) -> str:
        """Wait asynchronously for a node's output.

        If the output is already available, returns immediately. Otherwise suspends
        the calling coroutine until record() is called for this node_id, then resumes.

        Raises:
            TimeoutError: if output not available within timeout_sec
        """
        # Fast path: predecessor already completed.
        output = self._node_output_text.get(node_id)
        if output is not None:
            return output

        # Create the event before any await point. Because asyncio is cooperative,
        # record() cannot run between this line and the event.wait() below, so there
        # is no window where the event could be set before we start waiting.
        if node_id not in self._node_events:
            self._node_events[node_id] = asyncio.Event()
        event = self._node_events[node_id]

        # Second fast-path check: no await has occurred since the first check,
        # but be defensive in case this method's call site changes in the future.
        output = self._node_output_text.get(node_id)
        if output is not None:
            return output

        logger.debug(f"Node {node_id} waiting on asyncio.Event (zero threads)")

        try:
            await asyncio.wait_for(event.wait(), timeout=timeout_sec)
        except asyncio.TimeoutError as e:
            raise TimeoutError(
                f"NodeOutputRegistry: output for '{node_id}' not available after "
                f"{timeout_sec:.1f}s. Check that the predecessor is not blocked or failed."
            ) from e

        # record() writes _node_output_text before calling event.set(). Both run on
        # the same single-threaded event loop, so the value is guaranteed to be present here.
        output = self._node_output_text.get(node_id)
        assert output is not None, (
            f"asyncio.Event fired for {node_id} but output missing from local cache — this is a bug in record()"
        )
        logger.debug(f"Node {node_id} woke from asyncio.Event")
        return output


# ---------------------------------------------------------------------------
# OTelChatCompletionAPIData — captures output and registers it in the registry
# ---------------------------------------------------------------------------


class OTelChatCompletionAPIData(ChatCompletionAPIData):
    """ChatCompletionAPIData subclass for OTel trace replay.

    Overrides process_response to:
    1. Extract the actual output text from the LLM response.
    2. Register it in the NodeOutputRegistry under this call's node_id.
    3. Return OTelInferenceInfo (InferenceInfo subclass) with output_text populated.

    The registry and node_id are passed at construction time via Pydantic fields.
    arbitrary_types_allowed is required because NodeOutputRegistry and
    WorkerSessionTracker are not Pydantic models.

    Dependency handling:
    - predecessor_node_ids: List of nodes that must complete before this one
    - wait_ms: Additional delay after predecessors complete
    - input_segments: Segment decomposition for output substitution
    - original_messages: Original messages before substitution (used as template)
    """

    model_config = {"arbitrary_types_allowed": True}

    node_id: str
    registry: NodeOutputRegistry
    worker_tracker: WorkerSessionTracker

    # Session completion notification
    completion_queue: Any  # mp.Queue for notifying main process
    total_nodes_in_session: int  # Total number of nodes in this session

    # Dependency information for async waiting
    predecessor_node_ids: List[str] = field(default_factory=list)
    wait_ms: int = 0
    input_segments: List[InputSegment] = field(default_factory=list)
    original_messages: List[Dict[str, Any]] = field(default_factory=list)
    expected_output_content: Optional[str] = None

    # Flag to skip HTTP request (set when session fails)
    skip_request: bool = False

    def _extract_session_id(self) -> str:
        """Extract session_id from qualified node_id.

        Returns:
            The session_id portion of the node_id
        """
        return self.node_id.split(":")[0] if ":" in self.node_id else self.node_id


    async def wait_for_predecessors_and_substitute(self) -> None:
        """Wait for all predecessor nodes to complete, then substitute output segments.

        Called by the worker before dispatching the HTTP request. Each predecessor
        is awaited via asyncio.Event, so the coroutine suspends without consuming
        any OS threads. All predecessors are waited in parallel via asyncio.gather.

        Requires all nodes of this session to run on the same worker, which is
        enforced by session-to-worker affinity in get_session_events().
        """
        # Extract session_id from node_id (format: "session_id:node_xxx")
        session_id = self._extract_session_id()

        # Pre-wait check: if session already failed, skip immediately.
        if self.worker_tracker.is_session_failed(session_id):
            logger.info(f"Node {self.node_id} skipping - session {session_id} has failed (pre-wait check)")
            self.skip_request = True
            self.registry.record(self.node_id, "", self.messages)
            # Extract node_id from qualified node_id
            node_id = self.node_id.split(":", 1)[1] if ":" in self.node_id else self.node_id
            self.worker_tracker.record_node_completed(session_id, node_id, time.perf_counter())
            return

        if self.predecessor_node_ids:
            logger.debug(f"Node {self.node_id} waiting for {len(self.predecessor_node_ids)} predecessor(s)")
            # Wait for all predecessors in parallel before proceeding.
            await asyncio.gather(
                *[self.registry.require_async(node_id, timeout_sec=3600.0) for node_id in self.predecessor_node_ids]
            )
            logger.debug(f"Node {self.node_id} all predecessors done")

            # Post-wait check: a predecessor may have failed while we were waiting.
            if self.worker_tracker.is_session_failed(session_id):
                logger.info(f"Node {self.node_id} skipping - session {session_id} has failed (post-wait check)")
                self.skip_request = True
                self.registry.record(self.node_id, "", self.messages)
                # Extract node_id from qualified node_id
                node_id = self.node_id.split(":", 1)[1] if ":" in self.node_id else self.node_id
                self.worker_tracker.record_node_completed(session_id, node_id, time.perf_counter())
                return

        # Wait additional delay specified in wait_ms
        if self.wait_ms > 0:
            wait_sec = self.wait_ms / 1000.0
            logger.debug(f"Node {self.node_id} waiting {wait_sec:.3f}s (wait_ms={self.wait_ms})")
            await asyncio.sleep(wait_sec)

        # Substitute output segments with actual predecessor outputs.
        if any(seg.type == "output" or seg.type == "shared" for seg in self.input_segments):
            logger.debug(f"Node {self.node_id} substituting output/shared segments")
            substituted = self._build_messages_with_substitution()
            self.messages = [ChatMessage(role=m["role"], content=m["content"]) for m in substituted]
            logger.debug(f"Node {self.node_id} substitution complete, {len(self.messages)} messages")

    def _build_messages_with_substitution(self) -> List[Dict[str, Any]]:
        """Build messages with output segments substituted with actual predecessor outputs.

        For assistant messages, we substitute their output messages with the actual output.
        """
        if not self.input_segments:
            return self.original_messages

        result = []
        cursor = 0

        for seg in self.input_segments:
            seg_msgs = self.original_messages[cursor : cursor + seg.message_count]

            if seg.type == "output":
                # Output segment: substitute with actual output from the specific predecessor
                if seg.source_node_id:
                    actual_output = self.registry.get_output_by_node_id(seg.source_node_id)
                    logger.debug(
                        f"Registry get for node {self.node_id} output segment from {seg.source_node_id} generated: {actual_output}"
                    )

                    if actual_output:
                        for msg in seg_msgs:
                            substituted = dict(msg)
                            substituted["content"] = actual_output
                            result.append(substituted)
                        logger.debug(
                            f"Node {self.node_id}: substituted output segment with actual output from {seg.source_node_id}"
                        )
                    else:
                        logger.warning(
                            f"Node {self.node_id}: output segment from {seg.source_node_id} "
                            f"not available, using recorded content"
                        )
                        result.extend(seg_msgs)
                else:
                    logger.warning(f"Node {self.node_id}: output segment has no source_node_id, using recorded content")
                    result.extend(seg_msgs)
            elif seg.type == "shared":
                seg_msgs_from_parent = self.registry.get_messages_by_node_id(seg.source_node_id)
                if seg_msgs_from_parent is None:
                    logger.error(
                        f"CRITICAL: Node {self.node_id} shared segment from {seg.source_node_id} "
                        f"has no messages in registry (should not happen after require_async)"
                    )
                    # Fallback to original messages
                    result.extend(seg_msgs)
                else:
                    logger.debug(
                        f"Registry get for node {self.node_id} from {seg.source_node_id} "
                        f"shared segment num messages in parent node: {len(seg_msgs_from_parent)}"
                    )
                    if len(seg_msgs_from_parent) != len(seg_msgs):
                        logger.error(
                            f"Node {self.node_id} shared segment from {seg.source_node_id} "
                            f"had different number of messages in parent node: {len(seg_msgs_from_parent)}, "
                            f"num messages in seg: {len(seg_msgs)}"
                        )
                    for msg in seg_msgs_from_parent:
                        result.append(dict(msg))
            elif seg.type == "unique":
                # Unique message, keep as-is
                for msg in seg_msgs:
                    result.append(msg)
            else:
                # Unknown segment type, keep as-is
                result.extend(seg_msgs)

            cursor += seg.message_count

        return result

    def on_completion(self, info: InferenceInfo) -> None:
        """Called after every request completes (real or mock) to register output and unblock successors."""
        output_text = info.output_text if isinstance(info, OTelInferenceInfo) else ""
        output_text = output_text or ""
        self.registry.record(self.node_id, output_text, self.messages)
        logger.debug(
            f"calling registry record for node {self.node_id} num input messages {len(self.messages)} and output: {output_text}"
        )
        completion_time = time.perf_counter()
        session_id = self._extract_session_id()
        node_id = self.node_id.split(":", 1)[1] if ":" in self.node_id else self.node_id
        self.worker_tracker.record_node_completed(session_id, node_id, completion_time)
        logger.debug(f"Recorded node completion in worker tracker for {self.node_id}")

        # Check if this was the last node in the session
        completed_count = self.worker_tracker.get_session_node_count(session_id)

        if completed_count == self.total_nodes_in_session:
            # Session complete! Notify main process via queue
            logger.info(f"Session {session_id} completed all {self.total_nodes_in_session} nodes in worker")

            # Gather completion data (node_ids are already unqualified in nested structure)
            completion_data = {
                "session_id": session_id,
                "completion_time": completion_time,
                "failed": self.worker_tracker.is_session_failed(session_id),
                "node_completion_times": self.worker_tracker.get_session_completion_times(session_id),
            }

            # Push to queue for main process
            if self.completion_queue is not None:
                try:
                    self.completion_queue.put_nowait(completion_data)
                    logger.debug(f"Pushed session {session_id} completion to queue")
                except Exception as e:
                    logger.error(f"Failed to push session {session_id} completion to queue: {e}")

    async def process_response(
        self,
        response: ClientResponse,
        config: APIConfig,
        tokenizer: CustomTokenizer,
        lora_adapter: Optional[str] = None,
    ) -> OTelInferenceInfo:
        """Process the LLM response, capture output text, and register it."""
        logger.debug(f"process_response called for node {self.node_id}")
        output_text: str = ""

        if config.streaming:
            output_text = ""
            output_token_times: List[float] = []
            buffer = b""
            async for chunk in response.content.iter_any():
                buffer += chunk
                while b"\n\n" in buffer:
                    message, buffer = buffer.split(b"\n\n", 1)
                    output_token_times.append(time.perf_counter())
                    for line in message.split(sep=b"\n"):
                        if line.startswith(b"data:"):
                            data_str = line.removeprefix(b"data: ").strip()
                            if data_str == b"[DONE]":
                                break
                            try:
                                data = json.loads(data_str)
                                choices = data.get("choices", [])
                                if choices:
                                    delta = choices[0].get("delta", {})
                                    content = delta.get("content")
                                    if content:
                                        output_text += content
                            except (json.JSONDecodeError, IndexError):
                                continue
                    else:
                        continue
                    break

            prompt_text = "".join([msg.content for msg in self.messages if msg.content])
            prompt_len = tokenizer.count_tokens(prompt_text)
            output_len = tokenizer.count_tokens(output_text)
            info = OTelInferenceInfo(
                input_tokens=prompt_len,
                output_tokens=output_len,
                output_token_times=output_token_times,
                lora_adapter=lora_adapter,
                output_text=output_text or None,
            )
        else:
            data = await response.json()
            prompt_len = tokenizer.count_tokens("".join([m.content for m in self.messages]))
            choices = data.get("choices", [])
            if choices:
                output_text = "".join([choice.get("message", {}).get("content", "") for choice in choices])
            output_len = tokenizer.count_tokens(output_text)
            info = OTelInferenceInfo(
                input_tokens=prompt_len,
                output_tokens=output_len,
                lora_adapter=lora_adapter,
                output_text=output_text or None,
            )

        # Register output and notify successors.
        self.on_completion(info)

        if output_text:
            logger.debug(f"Registered output for node {self.node_id}: {len(output_text)} chars : {output_text}")
        else:
            logger.debug(f"Registered empty output for node {self.node_id}")

        return info

    async def process_failure(
        self,
        response: Optional[ClientResponse],
        config: APIConfig,
        tokenizer: CustomTokenizer,
        exception: Exception,
        lora_adapter: Optional[str] = None,
    ) -> InferenceInfo:
        """Process a failed request and signal failure to main process.

        This method is called when:
        1. An exception occurs (network error, timeout, etc.)
        2. An HTTP error occurs (status != 200)

        It signals the failure to the main process via shared state,
        then calls on_completion() to unblock dependent nodes.

        Args:
            response: The HTTP response (may be None for network errors)
            config: API configuration
            tokenizer: Tokenizer for token counting
            exception: The exception that occurred
            lora_adapter: Optional LoRA adapter name

        Returns:
            OTelInferenceInfo with empty output
        """
        logger.error(f"Request failed for node {self.node_id}: {type(exception).__name__}: {str(exception)}")

        # Extract session_id from node_id (format: "session_id:node_xxx")
        session_id = self._extract_session_id()

        # Mark the entire session as failed to prevent other nodes from processing
        self.worker_tracker.mark_session_failed(session_id)

        # Create empty inference info
        empty_info = OTelInferenceInfo(
            input_tokens=0,
            output_tokens=0,
            lora_adapter=lora_adapter,
            output_text="",  # Empty output for failed requests
        )

        # Call on_completion to register empty output and unblock dependents
        self.on_completion(empty_info)

        logger.info(f"Called on_completion() for failed node {self.node_id} to unblock dependent nodes")

        return empty_info


