"""
OTel Trace Replay Data Generator

This module provides a data generator that replays LLM requests from OpenTelemetry trace files.
It processes OTel JSON traces, extracts LLM calls, infers dependencies, and replays them
with proper timing and dependency enforcement.

Output-aware replay
-------------------
When a call has "output" segments (i.e., a predecessor's output was injected as an assistant
message in the original trace), the replayer substitutes the recorded assistant message content
with the *actual* output generated by the predecessor call at replay time.

This ensures that growing-context patterns (multi-turn conversations, agent chains) are replayed
faithfully: the KV cache sees the real generated text, not the static recorded text.

The substitution is performed in `load_lazy_data` using the `NodeOutputRegistry`, which maps
node_id → actual output text. All registry data is stored in per-worker plain dicts; session-to-
worker affinity ensures that the node writing an output and the nodes reading it always run on
the same worker process.

The actual output is captured in `OTelChatCompletionAPIData.process_response`, which overrides
the base `ChatCompletionAPIData.process_response` to register the output after each LLM call.

Cross-process coordination
---------------------------
Two orthogonal coordination problems exist in this system:

  Problem A — intra-worker, between coroutines on the same event loop:
    A node waits for its predecessor's output before building its request.
    Handled by NodeOutputRegistry using plain dicts and asyncio.Event.
    No IPC involved; session-to-worker affinity makes cross-process sharing unnecessary.

  Problem B — cross-process, worker → main process:
    The main process needs to know when sessions complete for OTEL span closure and reporting.
    Workers detect session completion and push notifications via mp.Queue.

    - WorkerSessionTracker: Per-worker tracking of node completions and session failures
    - session_completion_queue: Workers push completion data when last node finishes
    - Main process consumes from queue in check_session_completed()

OTelChatCompletionAPIData holds refs to WorkerSessionTracker and completion_queue.
"""

