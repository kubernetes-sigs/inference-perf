@dataclass
class _ParentPlan:
    trace_id: str
    normals: List[Tuple[int, Union[WekaNormalRequest, WekaStreamingRequest]]]
    subagents: List[Tuple[int, WekaSubagentEntry]]
    block_size: int


@dataclass
class _ChildPlan:
    session_id: str
    parent_trace_id: str
    subagent_index: int
    entry: WekaSubagentEntry
    stream_index: int
    stream_requests: List[WekaNormalRequest]
    block_size: int


@dataclass
class _TraceIdleTiming:
    parent_by_outer_idx: Dict[int, _RequestTiming]
    child_by_session_request: Dict[Tuple[str, int], _RequestTiming]
    subagent_end_by_outer_idx: Dict[int, float]
    subagent_start_by_outer_idx: Dict[int, float]


def _sa_end_seconds(entry: WekaSubagentEntry) -> float:
    duration = entry.duration_ms or 0
    return entry.t + duration / 1000.0


def _subagent_request_absolute_t(entry: WekaSubagentEntry, req: WekaNormalRequest) -> float:
    if req.t + 1e-6 < entry.t:
        return entry.t + req.t
    return req.t


def _pack_into_streams(requests: List[WekaNormalRequest]) -> List[List[WekaNormalRequest]]:
    sorted_reqs = sorted(requests, key=lambda r: r.t)
    streams: List[List[WekaNormalRequest]] = []
    stream_ends: List[float] = []
    for r in sorted_reqs:
        r_end = r.t + (r.api_time or 0.0)
        placed = False
        for i, end in enumerate(stream_ends):
            if end <= r.t:
                streams[i].append(r)
                stream_ends[i] = r_end
                placed = True
                break
        if not placed:
            streams.append([r])
            stream_ends.append(r_end)
    return streams


def _build_trace_idle_timing(
    plan: _ParentPlan,
    child_plans: List[_ChildPlan],
    cap_seconds: float,
) -> _TraceIdleTiming:
    if cap_seconds <= 0:
        parent_by_outer_idx = {}
        prev_t = None
        for outer_idx, req in plan.normals:
            delay_ms: Optional[float] = None if prev_t is None else (req.t - prev_t) * 1000.0
            parent_by_outer_idx[outer_idx] = _RequestTiming(req.t, delay_ms)
            prev_t = req.t

        child_by_session_request = {}
        child_plans_for_trace = [cp for cp in child_plans if cp.parent_trace_id == plan.trace_id]
        for cp in child_plans_for_trace:
            prev_child_t = None
            for k, req in enumerate(cp.stream_requests):
                t = _subagent_request_absolute_t(cp.entry, req)
                delay_ms = None if prev_child_t is None else (t - prev_child_t) * 1000.0
                child_by_session_request[(cp.session_id, k)] = _RequestTiming(t, delay_ms)
                prev_child_t = t

        subagent_end_by_outer_idx = {outer_idx: _sa_end_seconds(entry) for outer_idx, entry in plan.subagents}
        subagent_start_by_outer_idx = {outer_idx: entry.t for outer_idx, entry in plan.subagents}
        return _TraceIdleTiming(
            parent_by_outer_idx=parent_by_outer_idx,
            child_by_session_request=child_by_session_request,
            subagent_end_by_outer_idx=subagent_end_by_outer_idx,
            subagent_start_by_outer_idx=subagent_start_by_outer_idx,
        )

    request_starts: List[float] = [req.t for _, req in plan.normals]

    # Filter child plans for this trace
    child_plans_for_trace = [cp for cp in child_plans if cp.parent_trace_id == plan.trace_id]
    for cp in child_plans_for_trace:
        for req in cp.stream_requests:
            request_starts.append(_subagent_request_absolute_t(cp.entry, req))

    warp = _IdleGapTimeWarp(request_starts, cap_seconds)
    parent_by_outer_idx = {}
    prev_t = None
    for outer_idx, req in plan.normals:
        t = warp.map(req.t)
        delay_ms = None if prev_t is None else (t - prev_t) * 1000.0
        parent_by_outer_idx[outer_idx] = _RequestTiming(t, delay_ms)
        prev_t = t

    child_by_session_request = {}
    for cp in child_plans_for_trace:
        prev_child_t = None
        for k, req in enumerate(cp.stream_requests):
            t = warp.map(_subagent_request_absolute_t(cp.entry, req))
            delay_ms = None if prev_child_t is None else (t - prev_child_t) * 1000.0
            child_by_session_request[(cp.session_id, k)] = _RequestTiming(t, delay_ms)
            prev_child_t = t

    subagent_end_by_outer_idx = {outer_idx: warp.map(_sa_end_seconds(entry)) for outer_idx, entry in plan.subagents}
    subagent_start_by_outer_idx = {outer_idx: warp.map(entry.t) for outer_idx, entry in plan.subagents}
    return _TraceIdleTiming(
        parent_by_outer_idx=parent_by_outer_idx,
        child_by_session_request=child_by_session_request,
        subagent_end_by_outer_idx=subagent_end_by_outer_idx,
        subagent_start_by_outer_idx=subagent_start_by_outer_idx,
    )


