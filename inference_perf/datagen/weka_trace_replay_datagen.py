"""Weka Trace Replay Data Generator.

This module provides a data generator that replays Weka KV-cache-tester traces
(e.g., from Hugging Face semianalysisai/cc-traces-weka-*).

KV-Cache Fidelity & Prompt Reconstruction
-----------------------------------------
Weka traces record conversation sessions as a sequence of block hash IDs (representing fixed-size
token blocks, e.g. 16 tokens/block). They do not store the original raw text prompts or completions.
To evaluate the KV-cache scheduler/performance of a target server under identical hit/miss conditions,
the generator must reconstruct conversation prompt text that tokenizes to the exact sequence of block
hashes recorded in the trace.

1. Turn-by-Turn Reconstructor:
   For every request (turn) in a session, the generator uses a deterministic `HashIdRandomGenerator`
   seeded with a combination of base seed, conversation ID, and block hash ID. It samples words from
   a static, public domain corpus (Shakespeare) to construct the prompt and expected assistant response text.
   This guarantees that if the same block hash ID is visited on different turns or worker processes,
   it always maps to the exact same text tokens, producing identical KV-cache keys on the server.

2. Longest Common Prefix (LCP) and Branching:
   During multi-turn conversations, the user may shift topics or backtrack. The generator compares
   the block hashes of consecutive turns (`longest_common_prefix`).
   - If Turn 1 has hashes [H1, H2, H3] and Turn 2 has [H1, H2, H4], the LCP is 2.
   - The reconstructor uses this LCP to rewind the conversation history back to the end of block 2 (H2),
     evicting the text for block H3, before appending the new prompt block H4.
   - This ensures prompt-cache structural alignment matches the trace exactly.

3. History Substitution Rationale:
   The generator pre-reconstructs assistant response text based on the output token lengths in the trace.
   During live replay, the actual text generated by the server is discarded for the purpose of history building.
   Instead, the pre-reconstructed assistant text is appended to construct subsequent turn prompts.

   - Why? If the live model deviates by even 1 token in output length, it shifts the start index of the next
     user prompt. Because KV-cache blocks are aligned on fixed boundaries, this 1-token shift would cause a
     100% cache miss on all subsequent user prompt blocks.
   - History substitution preserves the exact token offsets and boundaries, guaranteeing 100% KV-cache alignment
     under deterministic (temperature=0) benchmarks. Caveat is that this is not always possible when benchmarking
     different models. But this approach is preserved so we can resemble the KV cache tester approach for this
     replay which is what we are trying to emulate.
"""

