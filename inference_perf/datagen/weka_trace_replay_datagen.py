class HashIdRandomGenerator:
    """Deterministic random generator scoped per (trace_id, hash_id)."""

    def __init__(self, base_seed: int):
        self.base_seed = base_seed
        self._trace_id: str = ""
        self._rng = random.Random()

    def set_trace_id(self, trace_id: str) -> None:
        self._trace_id = trace_id

    def reseed_for_hash_id(self, hash_id: int) -> None:
        seed_bytes = hashlib.sha256(f"{self.base_seed}:{self._trace_id}:{hash_id}".encode()).digest()
        self._rng.seed(int.from_bytes(seed_bytes[:8], "big"))

    def randrange(self, stop: int) -> int:
        return self._rng.randrange(stop)

    def choice(self, seq: list[Any]) -> Any:
        return self._rng.choice(seq)


def sample_tokens_from_corpus(
    corpus: List[int],
    num_tokens: int,
    rng_to_use: HashIdRandomGenerator,
    sep_token: Optional[int] = None,
) -> List[int]:
    """Sample tokens from the pre-tokenized corpus using the provided RNG."""
    corpus_len = len(corpus)
    tokens: List[int] = []

    if sep_token is not None:
        tokens.append(sep_token)
        num_tokens -= 1

    start = rng_to_use.randrange(corpus_len)
    end = start + num_tokens

    if end <= corpus_len:
        tokens.extend(corpus[start:end])
    else:
        tokens.extend(corpus[start:])
        tokens.extend(corpus[: end - corpus_len])

    return tokens


